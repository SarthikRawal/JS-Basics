// Array.prototype.include()

const fruits = ["apple", "banana", "orange", "mango"];

console.log(fruits.includes("apple")); // true
console.log(fruits.includes("grape")); // false (grape is not in the array)
console.log(fruits.includes("banana", 2)); //(search starts from index 2, includes banana)



